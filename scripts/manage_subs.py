def get_sub_text(match):
    global map_table
    key = match.group()
    #print(key)

    # remove # which indicates to capitalize first letter
    capitalize = False
    if key[0] == '#':
        assert(len(key) > 1)
        capitalize = True
        key = key[1:]

    # remove ";"s
    assert(len(key) > 2)
    key = key[1:-1]

    subbed_text = map_table[key] 
    if capitalize:
        subbed_text = subbed_text[0].upper() + subbed_text[1:] if len(subbed_text) > 1 else ""

    return subbed_text

